package org.betaconceptframework.astroboa.serializer;




/**
 * @author Gregory Chomatas (gchomatas@betaconcept.com)
 * @author Savvas Triantafyllou (striantafyllou@betaconcept.com)
 *
 */
public interface Serializer {

	void startElement(String name, boolean elementHasAttributes, boolean writeName);

	void endElement(String name, boolean closeStartTag, boolean createCloseTag);

	void startArray(String name);

	void endArray(String name);
	
	void writeAttribute(String name, String value);

	void writeContent(String content, boolean escape);

	String serialize();

	boolean prettyPrintEnabled();
	
	String getCurrentValue();
}
