package org.betaconceptframework.astroboa.console.scheduler;

import java.util.Date;

import org.betaconceptframework.astroboa.console.jsf.edit.draft.Draft;
import org.jboss.seam.ScopeType;
import org.jboss.seam.annotations.AutoCreate;
import org.jboss.seam.annotations.In;
import org.jboss.seam.annotations.Name;
import org.jboss.seam.annotations.Scope;
import org.jboss.seam.annotations.async.Asynchronous;
import org.jboss.seam.annotations.async.Expiration;
import org.jboss.seam.annotations.async.IntervalDuration;
import org.jboss.seam.async.QuartzTriggerHandle;

/**
 * @author Gregory Chomatas (gchomatas@betaconcept.com)
 * @author Savvas Triantafyllou (striantafyllou@betaconcept.com)
 * 
 */
@AutoCreate
@Scope(ScopeType.APPLICATION)
@Name("draftScheduler")
public class DraftScheduler {

	@In(create=true)
	private Draft draft;
	
	@Asynchronous
	public QuartzTriggerHandle scheduleEmptyDraftFromExpiredDraftItems(@Expiration Date when, @IntervalDuration Long interval) {
		
		//Remove items in draft older than 2 days
		draft.removeDraftItemsOlderThan(2 * 24);
		
		return null;
	}
}
